package com.codingbat.results;

public class String_1 {
	
	public String helloName(String name) {
		return "Hello " + name + "!";
	}
	
	public String makeAbba(String a, String b) {
		return a + b + b + a;
	}
	
	public String makeTags(String tag, String word) {
		return "<" + tag + ">" + word + "</" + tag + ">";
	}
}
